""" Shows bounding boxes but nothing else. """

import chombovis
c = chombovis.this()

for a in 'x','y','z':
    c.slice.toggleVisibility( on_off=0, axis=a )
c.grid.setDetail( 'Bounding boxes' )
c.reader.setVisibleLevelMax( c.reader.getNumberOfLevels() - 1 )

c.misc.guiUpdate()
